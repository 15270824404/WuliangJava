/*
 * 自动类型转换
 */
public class HelloWorld {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		  double firstAvg = 81.29;  //第一次平均分
		  double secondAvg;         //第二次平均分
		  int rise = 2;

		  secondAvg = firstAvg + rise;//自动类型转换
		        
		  System.out.println("第二次平均分是："  + secondAvg);

	}

}
