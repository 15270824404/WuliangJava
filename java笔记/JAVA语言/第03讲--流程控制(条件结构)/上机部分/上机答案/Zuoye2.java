/**
 * Zuoye2 switch结构
 * 根据手机的设定进行自动拨号
 */
public class Zuoye2 {
	public static void main(String[] args) {
		int no = 1;
		switch (no) {
		case 1:
			System.out.println("拨爸爸的号");
			break;
		case 2:
			System.out.println("拨妈妈的号");
			break;
		case 3:
			System.out.println("拨爷爷的号");
			break;
		case 4:
			System.out.println("拨奶奶的号");
			break;
		default:
			break;
		}
	}
}
